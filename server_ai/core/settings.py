# Postgres on Supabase — see manage.py load_dotenv for Smart-CSM/.env
#
# If `python manage.py migrate` fails with "could not translate host name":
#   - `db.<ref>.supabase.co` is often IPv6-only; Windows/psycopg2 often cannot use it.
#   - Fix: Supabase → Settings → Database → copy **Session pooler** URI (IPv4-friendly).
#     Put it in Smart-CSM/.env as SUPABASE_DATABASE_URL=postgresql://...
#     Use **Session** mode for DDL/migrations (Transaction pooler can break Django migrate).
#
# Priority: SUPABASE_DATABASE_URL (or DATABASE_URL) > SUPABASE_DB_PASSWORD + host fields > SQLite
_pg_url = (os.environ.get("SUPABASE_DATABASE_URL") or os.environ.get("DATABASE_URL") or "").strip()
if _pg_url:
    DATABASES = {"default": _django_pg_from_url(_pg_url)}
elif os.environ.get("SUPABASE_DB_PASSWORD"):
    _host = os.environ.get("SUPABASE_DB_HOST") or _default_db_host_from_supabase_api_url()
    if not _host:
        raise ValueError(
            "SUPABASE_DB_PASSWORD is set but the DB host is unknown. Set SUPABASE_DB_HOST, "
            "or VITE_SUPABASE_URL (https://<ref>.supabase.co), or use SUPABASE_DATABASE_URL."
        )
    DATABASES = {
        "default": {
            "ENGINE": "django.db.backends.postgresql",
            "NAME": os.environ.get("SUPABASE_DB_NAME", "postgres"),
            "USER": os.environ.get("SUPABASE_DB_USER", "postgres"),
            "PASSWORD": os.environ["SUPABASE_DB_PASSWORD"],
            "HOST": _host,
            "PORT": os.environ.get("SUPABASE_DB_PORT", "5432"),
            "OPTIONS": {"sslmode": os.environ.get("SUPABASE_DB_SSLMODE", "require")},
        }
    }
else:
    DATABASES = {
        "default": {
            "ENGINE": "django.db.backends.sqlite3",
            "NAME": BASE_DIR / "db.sqlite3",
        }
    }


