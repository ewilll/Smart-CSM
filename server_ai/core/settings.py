def _default_db_host_from_supabase_api_url() -> Optional[str]:
    """https://<ref>.supabase.co -> db.<ref>.supabase.co (must match the project for SUPABASE_DB_PASSWORD)."""
    raw = (os.environ.get("VITE_SUPABASE_URL") or os.environ.get("SUPABASE_URL") or "").strip()
    if not raw:
        return None
    host = urlparse(raw).hostname or ""
    if host.endswith(".supabase.co") and host.count(".") >= 2:
        ref = host.split(".")[0]
        return f"db.{ref}.supabase.co"
    return None


def _django_pg_from_url(url: str) -> dict:
    """Parse postgresql://... for Django (use Supabase Session pooler URI if direct db.* fails DNS)."""
    normalized = url.strip().replace("postgresql+psycopg2://", "postgresql://")
    u = urlparse(normalized)
    if u.scheme not in ("postgres", "postgresql"):
        raise ValueError(f"SUPABASE_DATABASE_URL must be postgres/postgresql, got: {u.scheme}")
    dbname = (u.path or "").lstrip("/") or "postgres"
    if not u.hostname:
        raise ValueError("SUPABASE_DATABASE_URL missing host")
    q = parse_qs(u.query)
    sslmode = (q.get("sslmode") or ["require"])[0]
    return {
        "ENGINE": "django.db.backends.postgresql",
        "NAME": dbname,
        "USER": unquote(u.username) if u.username else "postgres",
        "PASSWORD": unquote(u.password) if u.password else "",
        "HOST": u.hostname,
        "PORT": str(u.port or 5432),
        "OPTIONS": {"sslmode": sslmode},
    }


